<script>
    $(document).ready(function () {
        let currentIndex = 0;
        const structures = $('.structur1'); // Select all structures
        const totalStructures = structures.length;
        let interval;

        function animateStructure(current, next) {
            current.animate({ opacity: 0, bottom: '-40px' }, 1000, function () {
                current.addClass('hidden').css({ opacity: 1, bottom: '0' }); // Reset the current structure's CSS
                next.css({ opacity: 0, bottom: '-40px' }).removeClass('hidden').animate({ opacity: 1, bottom: '0' }, 1000);
            });
        }

        function showNextStructure() {
            const currentStructure = structures.eq(currentIndex);
            currentIndex = (currentIndex + 1) % totalStructures; // Move to the next index, wrap around if needed
            const nextStructure = structures.eq(currentIndex);
            animateStructure(currentStructure, nextStructure);
        }

        function startInterval() {
            interval = setInterval(showNextStructure, 15000); // Call showNextStructure every 15 seconds
        }

        // Initial call to show the first structure
        startInterval();

        // Event listener for close button
        $(document).on('click', '.close_review', function () {
            clearInterval(interval); // Clear the interval to stop automatic cycling
            const currentStructure = $(this).closest('.structur1');
            currentIndex = (currentIndex + 1) % totalStructures; // Move to the next index, wrap around if needed
            const nextStructure = structures.eq(currentIndex);
            animateStructure(currentStructure, nextStructure);
            setTimeout(startInterval, 15000); // Restart the interval after 15 seconds
        });
    });
</script>

